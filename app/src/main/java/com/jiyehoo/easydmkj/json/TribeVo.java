package com.jiyehoo.easydmkj.json;

public class TribeVo {

    private String tribeLevel;
    private String typeDesc;
    private String imageUrl;
    private String tribeId;
    private String name;
    private String member;
    public void setTribeLevel(String tribeLevel) {
        this.tribeLevel = tribeLevel;
    }
    public String getTribeLevel() {
        return tribeLevel;
    }

    public void setTypeDesc(String typeDesc) {
        this.typeDesc = typeDesc;
    }
    public String getTypeDesc() {
        return typeDesc;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }
    public String getImageUrl() {
        return imageUrl;
    }

    public void setTribeId(String tribeId) {
        this.tribeId = tribeId;
    }
    public String getTribeId() {
        return tribeId;
    }

    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }

    public void setMember(String member) {
        this.member = member;
    }
    public String getMember() {
        return member;
    }

}